int		ft_count_if(char **tab, int (*f)(char*))
{
	int	vendetta;
	int v;

	vendetta = 0;
	v = 0;
	while (tab[v] != '\0')
	{
		if (f(tab[v]) == 1)
		{
			vendetta++;
		}
		v++;
	}
	return (vendetta);
}
